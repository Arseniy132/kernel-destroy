class Player:
    def __init__(self, x, y):
        self.reset(x, y)

    def update(self, game_over):
        dx = 0
        dy = 0
        walk_cooldown = 5

        if game_over == 0:
            key = pygame.key.get_pressed()
            if key[pygame.K_F4] and not self.jumped and not self.in_air:
                self.vel_y = -50
            if key[pygame.K_UP] and not self.jumped and not self.in_air:
                self.vel_y = -15
                self.jumped = True
            if not key[pygame.K_UP] or not key[pygame.K_F4]:
                self.jumped = False
            if key[pygame.K_LEFT]:
                dx -= 5
                self.counter += 1
                self.direction = -1
            if key[pygame.K_RIGHT]:
                dx += 5
                self.counter += 1
                self.direction = 1
            if not key[pygame.K_LEFT] and not key[pygame.K_RIGHT]:
                self.counter = 0
                self.index = 0
                if self.direction == 1:
                    self.image = self.images_right[self.index]
                if self.direction == -1:
                    self.image = self.images_left[self.index]

            if self.counter > walk_cooldown:
                self.counter = 0
                self.index += 1
                if self.index >= len(self.images_right):
                    self.index = 0
                if self.direction == 1:
                    self.image = self.images_right[self.index]
                if self.direction == -1:
                    self.image = self.images_left[self.index]

            self.vel_y += 0.75
            if self.vel_y > 9:
                self.vel_y = 9
            dy += self.vel_y

            self.in_air = True

            for tile in world.block_list:
                # check x
                if tile[1].colliderect(self.rect.x + dx, self.rect.y, self.width, self.height):
                    dx = 0
                # check y
                if tile[1].colliderect(self.rect.x, self.rect.y + dy, self.width, self.height):
                    # jump
                    if self.vel_y < 0:
                        dy = tile[1].bottom - self.rect.top
                        self.vel_y = 0
                    # fall
                    elif self.vel_y >= 0:
                        dy = tile[1].top - self.rect.bottom
                        self.vel_y = 0
                        self.in_air = False
            for err_ in world.err_List:
                # check x
                if err_[1].colliderect(self.rect.x + dx, self.rect.y, self.width, self.height):
                    dx = 0
                # check y
                if err_[1].colliderect(self.rect.x, self.rect.y + dy, self.width, self.height):
                    # jump
                    if self.vel_y < 0:
                        dy = err_[1].bottom - self.rect.top
                        self.vel_y = 0
                    # fall
                    elif self.vel_y >= 0:
                        dy = err_[1].top - self.rect.bottom
                        self.vel_y = 0
                        self.in_air = False

            if pygame.sprite.spritecollide(self, enm_G, False):
                game_over = -1

            if pygame.sprite.spritecollide(self, lava_G, False):
                game_over = -1

            self.rect.x += dx
            self.rect.y += dy

        elif game_over == -1:
            self.image = self.dead_image
            self.rect.y -= 5

        screen.blit(self.image, self.rect)
        return game_over

    def reset(self, x, y):
        self.images_right = []
        self.images_left = []
        self.index = 0
        self.counter = 0
        for num in range(1, 5):
            img_right = pygame.image.load(f"img/guy{num}.png")
            img_right = pygame.transform.scale(img_right, (20, 40))
            img_left = pygame.transform.flip(img_right, True, False)
            self.images_right.append(img_right)
            self.images_left.append(img_left)
        self.dead_image = pygame.image.load("img/ghost.png")
        self.image = self.images_right[self.index]
        self.rect = self.image.get_rect()
        self.rect.x = x
        self.rect.y = y
        self.width = self.image.get_width()
        self.height = self.image.get_height()
        self.vel_y = 0
        self.jumped = False
        self.direction = 0
        self.in_air = True


class World:
    def __init__(self, data):
        self.block_list = []
        self.err_List = []

        row_count = 0
        for line in data:
            col_count = 0
            for block in line:
                if block == 1:
                    img = pygame.transform.scale(dirt_img, (block_size, block_size))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * block_size
                    img_rect.y = row_count * block_size
                    block = (img, img_rect)
                    self.block_list.append(block)
                if block == 2:
                    img = pygame.transform.scale(grass_img, (block_size, block_size))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * block_size
                    img_rect.y = row_count * block_size
                    block = (img, img_rect)
                    self.block_list.append(block)
                if block == 3:
                    enm = Enemy(col_count * block_size, row_count * block_size + 15)
                    enm_G.add(enm)
                if block == 6:
                    lava = Lava(col_count * block_size, row_count * block_size + (block_size // 2))
                    lava_G.add(lava)
                if block == 7:
                    img = pygame.transform.scale(error2, (block_size, block_size))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * block_size
                    img_rect.y = row_count * block_size
                    err_ = (img, img_rect)
                    self.err_List.append(err_)
                if block == 8:
                    ext = Exit(col_count * block_size, row_count * block_size)
                    ext_G.add(ext)
                if block == 9:
                    err = Error(col_count * block_size, row_count * block_size)
                    err_G.add(err)

                col_count += 1
            row_count += 1

    def draw(self):
        for block in self.block_list:
            screen.blit(block[0], block[1])

        for err_ in self.err_List:
            screen.blit(err_[0], err_[1])


class Enemy(pygame.sprite.Sprite):
    def __init__(self, x, y):
        pygame.sprite.Sprite.__init__(self)
        self.image = pygame.image.load("img/blob.png")
        self.rect = self.image.get_rect()
        self.rect.x = x
        self.rect.y = y
        self.move_direction = 1
        self.move_counter = 0

    def update(self):
        self.rect.x += self.move_direction
        if self.move_counter > 50:
            self.move_direction = -1
            self.move_counter -= 1
        elif self.move_counter < 0:
            self.move_direction = 1
            self.move_counter += 1
        else:
            self.move_counter += self.move_direction


